"""01_variables."""

# interger
A = 10
print(A)

# float
B = 3.14
print(B)

# complex
C = 1 + 2j
print(C)

# arithmetic
RESULT = (A + B) * C
print(RESULT)

# string
D = "hello"
print(D)
print(D * 2)
print(D + " world")

# boolean
E = True
print(E)

# list
f = [1, 2, 3]
print(f)

# tuple
g = (1, 2, 3)
print(g)

# dictionary
h = {"a": 1, "b": 2, "c": 3}
print(h)

# set
i = {1, 2, 3}
print(i)

# None
j = None
print(j)

# type
print(type(A))
print(type(B))
print(type(C))
print(type(D))
print(type(E))
print(type(f))
print(type(g))
print(type(h))
print(type(i))
print(type(j))
