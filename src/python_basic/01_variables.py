"""01 variables."""

# integer
A = 10
print(A)

# float
B = 3.14
print(B)

# complex
C = 1 + 2j
print(C)

# arithmetic
RESULT = (A + B) * C
print(RESULT)

# string
D = "hello"
print(D)
print(D * 2)
print(D + " world")

# boolean
E = True
print(E)

# list
f = [1, 2, 3]
class_members = ["minh", "huy"]
print(f)
print(f[0])
f[0] = 10
print(f)

# tuple, prioritize to use (immutable)
g = (1, 2, 3)
print(g)
print(g[0])

# set
i = {3, 2, 1}
print(i)

# dictionary
h = {1: "hello", "b": 2, "c": 3}
print(h)


# None
j = None
print(j)

# type
print(type(A))
print(type(B))
print(type(C))
print(type(D))
print(type(E))
print(type(f))
print(type(g))
print(type(h))
print(type(i))
print(type(j))
